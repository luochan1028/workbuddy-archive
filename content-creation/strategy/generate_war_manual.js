function title(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 400, after: 200 },
    children: [new TextRun({ text, bold: true, size: 32, color: BRAND_PURPLE, font: "Microsoft YaHei" })],
  });
}

function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 300, after: 150 },
    children: [new TextRun({ text, bold: true, size: 26, color: BRAND_DARK, font: "Microsoft YaHei" })],
  });
}

function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 200, after: 100 },
    children: [new TextRun({ text, bold: true, size: 22, color: BRAND_PURPLE, font: "Microsoft YaHei" })],
  });
}

function p(text, opts = {}) {
  return new Paragraph({
    spacing: { before: 80, after: 80 },
    indent: opts.indent ? { left: 400 } : undefined,
    children: [new TextRun({ text, size: 21, color: opts.color || BRAND_GRAY, font: "Microsoft YaHei", bold: opts.bold || false })],
  });
}

function boldP(text) {
  return p(text, { bold: true, color: BRAND_DARK });
}

function redP(text) {
  return p(text, { color: BRAND_RED, bold: true });
}

function greenP(text) {
  return p(text, { color: BRAND_GREEN });
}

function blueP(text) {
  return p(text, { color: BRAND_BLUE });
}

function bulletItem(text, level = 0) {
  return new Paragraph({
    spacing: { before: 40, after: 40 },
    indent: { left: 400 + level * 300 },
    children: [
      new TextRun({ text: "\u2022 ", size: 21, color: BRAND_PURPLE, font: "Microsoft YaHei" }),
      new TextRun({ text, size: 21, color: BRAND_GRAY, font: "Microsoft YaHei" }),
    ],
  });
}

function numberedItem(num, text) {
  return new Paragraph({
    spacing: { before: 40, after: 40 },
    indent: { left: 400 },
    children: [
      new TextRun({ text: `${num}. `, size: 21, color: BRAND_PURPLE, font: "Microsoft YaHei", bold: true }),
      new TextRun({ text, size: 21, color: BRAND_GRAY, font: "Microsoft YaHei" }),
    ],
  });
}

function divider() {
  return new Paragraph({
    spacing: { before: 200, after: 200 },
    children: [new TextRun({ text: "\u2500".repeat(60), size: 16, color: "D3D1C7", font: "Microsoft YaHei" })],
  });
}

function makeRow(cells, isHeader = false) {
  return new TableRow({
    children: cells.map(cellText =>
      new TableCell({
        shading: isHeader ? { type: ShadingType.SOLID, color: "EEEDFE" } : undefined,
        children: [
          new Paragraph({
            spacing: { before: 40, after: 40 },
            children: [new TextRun({ text: cellText, size: 19, bold: isHeader, color: isHeader ? BRAND_PURPLE : BRAND_GRAY, font: "Microsoft YaHei" })],
          }),
        ],
      })
    ),
  });
}

function makeTable(headers, rows) {
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [makeRow(headers, true), ...rows.map(r => makeRow(r))],
  });
}

